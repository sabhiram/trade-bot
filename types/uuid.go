package types

////////////////////////////////////////////////////////////////////////////////

import (
	"crypto/rand"
	"fmt"
)

////////////////////////////////////////////////////////////////////////////////

const (
	UUIDLength = 16
)

////////////////////////////////////////////////////////////////////////////////

type UUID string

func NewUUID() UUID {
	b := make([]byte, UUIDLength)
	rand.Read(b)
	return UUID(fmt.Sprintf("%X-%X-%X-%X-%X", b[0:4], b[4:6], b[6:8], b[8:10], b[10:16]))
}

